//Operaciones aritmeticas//


class CAritmetica
{
	/*
	*Operaciones aritm√©ticas
	*/
	public static void main (String[] args)
	{
	int dato1, dato2, resultado;
	
	dato1 = 5;
	dato2 = 4;
	
	//Suma
	resultado = dato1 + dato2;
	System.out.println(dato1 + " - " + dato2 + " = " + resultado);
	
	//Resta
	resultado = dato1 * dato2;
	System.out.println(dato1 + " - " + dato2 + " = " + resultado);
	
	//Producto
	resultado = dato1 * dato2;
	System.out.println(dato1 + " * " + dato2 + " = " + resultado);
	
	//Cociente
	resultado = dato1 / dato2;
	System.out.println(dato1 + " / " + dato2 + " = " + resultado);
	}
}
	